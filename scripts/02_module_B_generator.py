# PRICING SEGMENTS ENGINE
# ============================================================

def generate_pricing_segments(policies: List[Dict], cfg: dict) -> List[Dict]:
    """
    Build simple pricing segments by (product_line, state).
    Expected columns for Module C:
      - segment_id, product_line, state,
        technically_required_premium, our_average_premium,
        market_average_premium, exposure_count

    Nota:
    - segment_id sigue siendo product_line-state para mantener
      compatibilidad con el schema de warehouse.
    """
    if not policies:
        return []

    df_p = pd.DataFrame(policies)

    grouped = df_p.groupby(["product_line", "state"], as_index=False).agg(
        coverage_mean=("coverage_amount", "mean"),
        premium_mean=("premium_annual", "mean"),
        exposure_count=("policy_id", "count")
    )

    trp_cfg = cfg["pricingModel"]["technicallyRequiredPremium"]
    market_var_cfg = cfg["pricingModel"]["marketPremiumVariation"]
    min_var = market_var_cfg["minPercent"]
    max_var = market_var_cfg["maxPercent"]

    rows: List[Dict] = []

    for _, row in grouped.iterrows():
        product_line = str(row["product_line"])
        state = str(row["state"])
        coverage_mean = float(row["coverage_mean"])
        our_avg_premium = float(row["premium_mean"])
        exposure_count = int(row["exposure_count"])

        pl_cfg = trp_cfg[product_line]
        base_rate = pl_cfg.get("baseRate", 0.03)
        base_factor = pl_cfg.get("baseFactor", 1.5)

        pure_premium = coverage_mean * base_rate
        technically_required_premium = pure_premium * base_factor

        # market premium with variation
        variation = random.uniform(min_var, max_var)
        market_average_premium = our_avg_premium * (1 + variation)

        segment_id = f"{product_line}-{state}"

        rows.append({
            "segment_id": segment_id,
            "product_line": product_line,
            "state": state,
            "technically_required_premium": round(technically_required_premium, 2),
            "our_average_premium": round(our_avg_premium, 2),
            "market_average_premium": round(market_average_premium, 2),
            "exposure_count": exposure_count
        })

    return rows


