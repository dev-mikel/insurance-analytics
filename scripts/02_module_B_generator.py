def ensure_dir(path: str) -> None:
    """Create directory if it does not exist."""
    os.makedirs(path, exist_ok=True)


def load_config(path: str = CONFIG_PATH) -> dict:
    """Load configuration generated by Module A."""
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Config file not found at {path}. Run Module A first."
        )
    with open(path, "r") as f:
        return json.load(f)


def save_raw_csv(name: str, rows: List[Dict]) -> None:
    """
    Save a list of dicts to CSV under output/raw.
    Example: name='customers' -> output/raw/customers.csv
    """
    ensure_dir(RAW_DIR)
    df = pd.DataFrame(rows)
        # For reproducibility of column order
    out_path = os.path.join(RAW_DIR, f"{name}.csv")
    df.to_csv(out_path, index=False)
    print(f"[OK] Saved raw file: {out_path} ({len(df)} rows)")


def random_choice_from_distribution(dist: Dict[str, float]) -> str:
    """
    Given a dictionary {value: weight}, return one value using the weights.
    Uses random.choices for clarity.
    """
    keys = list(dist.keys())
    weights = list(dist.values())
    return random.choices(population=keys, weights=weights, k=1)[0]


def random_date_in_year_range(start_year: int, end_year: int) -> datetime:
    """Return a random date between 1 Jan start_year and 31 Dec end_year."""
    year = random.randint(start_year, end_year)
    month = random.randint(1, 12)
    # to avoid edge cases with Feb/31, use day <= 28
    day = random.randint(1, 28)
    return datetime(year, month, day)


def add_days(base_date: datetime, mean_days: float, std_days: float) -> datetime:
    """
    Add a random number of days to base_date using a normal distribution.
    Result is clamped to >= 0 days.
    """
    delta = max(int(np.random.normal(mean_days, std_days)), 0)
    return base_date + timedelta(days=delta)


def clamp(value: float, min_value: float, max_value: float) -> float:
    """Clamp a numeric value into [min_value, max_value]."""
    return max(min_value, min(max_value, value))


