def generate_customers(cfg: dict) -> List[Dict]:
    """
    Generate synthetic customers based on customerModel and geography
    from the configuration.
    Expected columns for Module C:
    - customer_id, age, gender, occupation, marital_status,
      income, bmi, region, state, acquisition_channel

    Extra columns (safe for Module C to ignore):
    - acquisition_date (approx. first contact date)
    """
    num_customers = cfg["portfolio"]["numCustomers"]
    cust_cfg = cfg["customerModel"]
    geo_cfg = cfg["geography"]

    age_min = cust_cfg["age"]["min"]
    age_max = cust_cfg["age"]["max"]

    income_min = cust_cfg["income"]["min"]
    income_max = cust_cfg["income"]["max"]

    bmi_min = cust_cfg["bmi"]["min"]
    bmi_max = cust_cfg["bmi"]["max"]

    gender_dist = cust_cfg["genderDistribution"]
    occupation_dist = cust_cfg["occupationDistribution"]
    marital_dist = cust_cfg["maritalStatusDistribution"]
    acq_dist = cust_cfg["acquisitionChannels"]

    # geography
    regions_map = geo_cfg["regions"]
    region_dist = geo_cfg["regionDistribution"]

    # acquisition date config (approx: a bit before policy effective range)
    policy_date_cfg = cfg["policyModel"]["effectiveDateRange"]
    start_year = policy_date_cfg["startYear"]
    end_year = policy_date_cfg["endYear"]
    acq_start_year = max(start_year - 3, start_year)  # at most 3 years before
    acq_end_year = end_year

    customers: List[Dict] = []

    for i in range(num_customers):
        customer_id = f"CUST-{i+1:07d}"

        # region and state
        region = random_choice_from_distribution(region_dist)
        states_for_region = regions_map[region]
        state = random.choice(states_for_region)

        age = random.randint(age_min, age_max)
        income = random.randint(income_min, income_max)
        bmi = round(random.uniform(bmi_min, bmi_max), 1)

        gender = random_choice_from_distribution(gender_dist)
        occupation = random_choice_from_distribution(occupation_dist)
        marital_status = random_choice_from_distribution(marital_dist)
        acquisition_channel = random_choice_from_distribution(acq_dist)

        acquisition_date = random_date_in_year_range(acq_start_year, acq_end_year)

        customers.append({
            "customer_id": customer_id,
            "age": age,
            "gender": gender,
            "occupation": occupation,
            "marital_status": marital_status,
            "income": income,
            "bmi": bmi,
            "region": region,
            "state": state,
            "acquisition_channel": acquisition_channel,
            "acquisition_date": acquisition_date.strftime("%Y-%m-%d")
        })

    return customers


