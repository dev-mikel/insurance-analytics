def compute_policy_risk_score(
    coverage_amount: float,
    deductible: float,
    cov_cfg: dict,
    product_line: str,
    policy_model_cfg: dict
) -> Tuple[float, str]:
    """
    Compute a simple 0–100 risk_score and qualitative risk_class
    based on coverage, deductible and product-line specific risk factors.
    """
    cov_min = cov_cfg["min"]
    cov_max = cov_cfg["max"]
    # normalize coverage within its range
    coverage_span = max(cov_max - cov_min, 1)
    coverage_ratio = (coverage_amount - cov_min) / coverage_span
    coverage_ratio = clamp(coverage_ratio, 0.0, 1.0)

    # deductible relative to coverage
    deductible_ratio = clamp(deductible / max(coverage_amount, 1.0), 0.0, 1.0)

    # product risk factor from config (optional)
    product_risk_cfg = policy_model_cfg.get("productRiskFactor", {})
    product_risk_boost = float(product_risk_cfg.get(product_line, 0.0))

    # Simple linear model:
    # higher coverage -> more risk
    # lower deductible -> more risk
    base = 20.0
    score = (
        base
        + 50.0 * coverage_ratio
        + 30.0 * (1.0 - deductible_ratio)
        + product_risk_boost
    )
    score = clamp(score, 0.0, 100.0)

    if score < 40:
        risk_class = "LOW"
    elif score < 70:
        risk_class = "MEDIUM"
    else:
        risk_class = "HIGH"

    return score, risk_class


def generate_policies(customers: List[Dict], cfg: dict) -> List[Dict]:
    """
    Generate policies linked to customers.
    Expected columns for Module C:
    - policy_id, customer_id, product_line, coverage_amount,
      deductible, premium_annual, payment_frequency,
      start_date, end_date, region, state, tenure_years

    Extra columns (safe for Module C to ignore):
    - risk_score (0–100), risk_class (LOW/MEDIUM/HIGH)
    """
    product_mix = cfg["productMix"]
    policy_model_cfg = cfg["policyModel"]
    coverage_ranges = policy_model_cfg["coverageRanges"]
    deductible_ranges = policy_model_cfg["deductibleRanges"]
    prem_cfg = cfg["pricingModel"]["technicallyRequiredPremium"]
    date_cfg = policy_model_cfg["effectiveDateRange"]
    payment_freq_dist = policy_model_cfg["paymentFrequencyDistribution"]
    tenure_default_years = policy_model_cfg["tenureYearsDefault"]

    num_customers = len(customers)
    avg_pols = cfg["portfolio"]["avgPoliciesPerCustomer"]
    expected_policies = int(num_customers * avg_pols)

    policies: List[Dict] = []

    for i in range(expected_policies):
        cust = customers[i % num_customers]
        cust_id = cust["customer_id"]
        region = cust["region"]
        state = cust["state"]

        product_line = random_choice_from_distribution(product_mix)

        cov_cfg = coverage_ranges[product_line]
        ded_cfg = deductible_ranges[product_line]
        prem_line_cfg = prem_cfg[product_line]

        coverage_amount = random.randint(cov_cfg["min"], cov_cfg["max"])
        # ensure deductible < coverage_amount and >= 0
        deductible = random.randint(ded_cfg["min"], ded_cfg["max"])
        if deductible >= coverage_amount:
            # cap deductible to at most 50% of coverage as a safety rule
            deductible = int(coverage_amount * 0.5)

        deductible = max(deductible, 0)

        base_rate = prem_line_cfg.get("baseRate", 0.03)
        base_factor = prem_line_cfg.get("baseFactor", 1.5)

        pure_premium = coverage_amount * base_rate
        noise = random.uniform(0.9, 1.1)
        premium_annual = pure_premium * base_factor * noise
        # ensure strictly positive and not absurd vs coverage
        premium_annual = clamp(premium_annual, 50.0, coverage_amount * 0.5)
        premium_annual = round(premium_annual, 2)

        start_dt = random_date_in_year_range(
            date_cfg["startYear"], date_cfg["endYear"]
        )
        # Simple logic: policy term = tenure_default_years * 365 days
        end_dt = start_dt + timedelta(days=365 * tenure_default_years)

        payment_frequency = random_choice_from_distribution(payment_freq_dist)

        # risk scoring
        risk_score, risk_class = compute_policy_risk_score(
            coverage_amount,
            deductible,
            cov_cfg,
            product_line,
            policy_model_cfg
        )

        policy = {
            "policy_id": f"POL-{i+1:07d}",
            "customer_id": cust_id,
            "product_line": product_line,
            "coverage_amount": coverage_amount,
            "deductible": deductible,
            "premium_annual": premium_annual,
            "payment_frequency": payment_frequency,
            "start_date": start_dt.strftime("%Y-%m-%d"),
            "end_date": end_dt.strftime("%Y-%m-%d"),
            "region": region,
            "state": state,
            "tenure_years": tenure_default_years,
            "risk_score": round(risk_score, 2),
            "risk_class": risk_class
        }
        policies.append(policy)

    return policies


