# SEVERITY SAMPLER (CLAIMS)
# ============================================================

def sample_severity(product_line: str, sev_cfg: Dict[str, dict]) -> float:
    """
    Sample a claim severity value given line of business and config.
    Supports lognormal, gamma and fixed distributions.
    """
    cfg = sev_cfg[product_line]

    dist = cfg.get("distribution")
    if dist == "lognormal":
        return float(np.random.lognormal(cfg["mu"], cfg["sigma"]))
    if dist == "gamma":
        return float(np.random.gamma(cfg["shape"], cfg["scale"]))
    if dist == "fixed":
        return float(cfg["value"])

    # Fallback, should not happen if config is correct
    return 0.0


