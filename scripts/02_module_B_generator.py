# QUOTES + UNDERWRITING DECISIONS ENGINE
# ============================================================

def generate_quotes_and_uw_decisions(
    policies: List[Dict],
    cfg: dict
) -> (List[Dict], List[Dict]):
    """
    Generate quotes and underwriting decisions.
    Quotes expected by Module C:
      - quote_id, customer_id, product_line, policy_id_target,
        request_date, quote_date, quote_status, region, state

    UW decisions expected by Module C:
      - quote_id, stp_flag, accepted_flag, bound_flag,
        expired_flag, decline_reason

    Mejora:
    - stp_flag depende de risk_class (más STP en bajo riesgo).
    - flags se recalculan al final a partir de quote_status
      para garantizar consistencia.
    """
    portfolio_cfg = cfg["portfolio"]
    quote_multiplier = portfolio_cfg["quoteMultiplier"]

    uw_cfg = cfg["underwriting"]
    funnel_probs = uw_cfg["funnelProbabilities"]

    num_policies = len(policies)
    expected_quotes = int(num_policies * quote_multiplier)

    quotes: List[Dict] = []
    uw_decisions: List[Dict] = []

    # simple decline reason distribution
    decline_reasons_dist = {
        "Price": 0.35,
        "Risk": 0.25,
        "Documentation": 0.15,
        "Customer Decision": 0.15,
        "Other": 0.10
    }

    base_stp_prob = uw_cfg.get("baseStpProbability", 0.30)

    for i in range(expected_quotes):
        pol = policies[i % num_policies]

        quote_id = f"Q-{i+1:07d}"
        customer_id = pol["customer_id"]
        product_line = pol["product_line"]
        policy_id = pol["policy_id"]
        region = pol["region"]
        state = pol["state"]
        risk_class = pol.get("risk_class", "MEDIUM")

        # request date slightly before or at policy start
        start_dt = datetime.strptime(pol["start_date"], "%Y-%m-%d")
        request_dt = start_dt - timedelta(days=random.randint(0, 60))
        quote_dt = request_dt + timedelta(days=random.randint(0, 5))

        # Funnel probabilities
        requested_to_quoted_p = funnel_probs["Requested_to_Quoted"]
        quoted_to_accepted_p = funnel_probs["Quoted_to_Accepted"]
        accepted_to_bound_p = funnel_probs["Accepted_to_Bound"]
        quoted_to_declined_p = funnel_probs["Quoted_to_Declined"]
        accepted_to_expired_p = funnel_probs["Accepted_to_Expired"]

        # STP as function of risk_class
        if risk_class == "LOW":
            stp_prob = clamp(base_stp_prob * 1.8, 0.0, 0.9)
        elif risk_class == "HIGH":
            stp_prob = clamp(base_stp_prob * 0.5, 0.0, 0.9)
        else:
            stp_prob = base_stp_prob

        stp_flag = random.random() < stp_prob

        decline_reason = ""
        quote_status = "REQUESTED"

        # Step 1: Requested -> Quoted?
        if random.random() <= requested_to_quoted_p:
            # Step 2: Quoted -> Accepted?
            if random.random() <= quoted_to_accepted_p:
                # Step 3: Accepted -> Bound?
                if random.random() <= accepted_to_bound_p:
                    quote_status = "BOUND"
                else:
                    # Maybe expires after acceptance
                    if random.random() <= accepted_to_expired_p:
                        quote_status = "EXPIRED"
                    else:
                        quote_status = "ACCEPTED"
            else:
                # Not accepted, maybe declined
                if random.random() <= quoted_to_declined_p:
                    quote_status = "DECLINED"
                    decline_reason = random_choice_from_distribution(
                        decline_reasons_dist
                    )
                else:
                    quote_status = "QUOTED"
        else:
            # Never quoted, treat as expired request
            quote_status = "EXPIRED"

        # Flags derivados de quote_status para garantizar coherencia
        accepted_flag = quote_status in ("ACCEPTED", "BOUND")
        bound_flag = quote_status == "BOUND"
        expired_flag = quote_status == "EXPIRED"

        quote_row = {
            "quote_id": quote_id,
            "customer_id": customer_id,
            "product_line": product_line,
            "policy_id_target": policy_id,
            "request_date": request_dt.strftime("%Y-%m-%d"),
            "quote_date": quote_dt.strftime("%Y-%m-%d"),
            "quote_status": quote_status,
            "region": region,
            "state": state
        }
        quotes.append(quote_row)

        uw_row = {
            "quote_id": quote_id,
            "stp_flag": stp_flag,
            "accepted_flag": accepted_flag,
            "bound_flag": bound_flag,
            "expired_flag": expired_flag,
            "decline_reason": decline_reason
        }
        uw_decisions.append(uw_row)

    return quotes, uw_decisions


