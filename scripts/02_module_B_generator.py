def generate_loss_development(claims: List[Dict], cfg: dict) -> List[Dict]:
    """
    Generate simplified loss development rows based on claims.
    Expected columns for Module C:
    - claim_id, product_line, state,
      development_month, incurred_losses, paid_losses

    Nota:
    - con growth rates positivos, incurred y paid son monotónicos crecientes.
    """
    ld_cfg = cfg["pricingModel"]["lossDevelopment"]
    periods = ld_cfg["periods"]
    incurred_growth = ld_cfg["incurredGrowthRate"]
    paid_growth = ld_cfg["paidGrowthRate"]

    rows: List[Dict] = []

    for cl in claims:
        base_incurred = cl["claim_amount"]
        base_paid = cl["net_paid"]
        product_line = cl["product_line"]
        state = cl["state"]

        for m in periods:
            factor_incurred = (1 + incurred_growth) ** (m / 12.0)
            factor_paid = (1 + paid_growth) ** (m / 12.0)

            incurred = base_incurred * factor_incurred
            paid = base_paid * factor_paid

            rows.append({
                "claim_id": cl["claim_id"],
                "product_line": product_line,
                "state": state,
                "development_month": m,
                "incurred_losses": round(float(incurred), 2),
                "paid_losses": round(float(paid), 2)
            })

    return rows


