def generate_claims(policies: List[Dict], cfg: dict) -> List[Dict]:
    """
    Generate claims based on policies and claim configuration.
    Expected columns for Module C:
    - claim_id, policy_id, customer_id, product_line,
      coverage_amount, claim_amount, deductible, net_paid,
      occurrence_date, report_date, settlement_date,
      region, state, fraud_score, legal_rep_flag

    Mejora clave:
    - frecuencia por póliza con Poisson, con lambda ajustada para
      respetar expectedClaims del config.
    - severidad acotada por cobertura (no siniestros absurdos).
    - fraud_score refinado con risk_class y legal_rep.
    """
    claims_model = cfg["claimsModel"]
    sev_cfg = claims_model["severityCurves"]
    reporting_delay_cfg = claims_model["reportingDelay"]
    settlement_delay_cfg = claims_model["settlementDelay"]

    claim_rate = cfg["portfolio"]["claimRateAnnual"]
    years = cfg["portfolio"]["historyYears"]
    expected_claims = cfg["portfolio"]["expectedClaims"]

    # multiplicadores opcionales
    freq_by_lob = claims_model.get("frequencyByProductLine", {})
    freq_by_region = claims_model.get("frequencyByRegion", {})

    # --------------------------------------------------------
    # 1) Calculamos lambdas sin escalar por póliza
    # --------------------------------------------------------
    raw_lambdas: List[float] = []
    for pol in policies:
        product_line = pol["product_line"]
        region = pol["region"]
        tenure_years = pol.get("tenure_years", years)

        lob_mult = float(freq_by_lob.get(product_line, 1.0))
        reg_mult = float(freq_by_region.get(region, 1.0))

        lam = claim_rate * tenure_years * lob_mult * reg_mult
        raw_lambdas.append(lam)

    # objetivo global similar a expected_claims
    base_total_lambda = len(policies) * claim_rate * years
    target_total_lambda = float(expected_claims or base_total_lambda)

    sum_raw = sum(raw_lambdas)
    scale = target_total_lambda / sum_raw if sum_raw > 0 else 1.0
    scaled_lambdas = [max(l * scale, 0.0) for l in raw_lambdas]

    claims: List[Dict] = []
    claim_counter = 0

    for pol, lam in zip(policies, scaled_lambdas):
        policy_start = datetime.strptime(pol["start_date"], "%Y-%m-%d")
        policy_end = datetime.strptime(pol["end_date"], "%Y-%m-%d")
        total_days = max((policy_end - policy_start).days, 1)

        product_line = pol["product_line"]
        coverage_amount = pol["coverage_amount"]
        deductible = pol["deductible"]
        region = pol["region"]
        state = pol["state"]
        risk_class = pol.get("risk_class", "MEDIUM")

        # número de siniestros para esta póliza
        n_claims = np.random.poisson(lam)
        if n_claims <= 0:
            continue

        for _ in range(n_claims):
            claim_counter += 1

            # occurrence dentro del periodo de cobertura
            offset = random.randint(0, total_days - 1)
            occurrence_dt = policy_start + timedelta(days=offset)

            report_dt = add_days(
                occurrence_dt,
                reporting_delay_cfg["meanDays"],
                reporting_delay_cfg["stdDays"]
            )
            if report_dt < occurrence_dt:
                report_dt = occurrence_dt

            settlement_dt = add_days(
                report_dt,
                settlement_delay_cfg["meanDays"],
                settlement_delay_cfg["stdDays"]
            )
            if settlement_dt < report_dt:
                settlement_dt = report_dt

            # severidad muestral
            raw_claim_amount = sample_severity(product_line, sev_cfg)
            # cap a un múltiplo razonable de la cobertura
            max_factor = claims_model.get("maxSeverityFactor", 1.0)
            max_allowed = coverage_amount * max_factor
            claim_amount = clamp(raw_claim_amount, 0.0, max_allowed)
            claim_amount = round(float(claim_amount), 2)

            net_paid = max(claim_amount - deductible, 0.0)
            net_paid = clamp(net_paid, 0.0, coverage_amount)
            net_paid = round(float(net_paid), 2)

            # fraud score refinado
            report_delay_days = (report_dt - occurrence_dt).days
            severity_ratio = claim_amount / max(coverage_amount, 1.0)

            base_fraud = 20 + report_delay_days * 2 + severity_ratio * 30

            # boost por legal rep y riesgo alto
            legal_rep_flag = random.random() < 0.15
            if legal_rep_flag:
                base_fraud += 10

            if risk_class == "HIGH":
                base_fraud += 5
            elif risk_class == "LOW":
                base_fraud -= 5

            fraud_score = int(round(clamp(base_fraud, 0.0, 100.0)))

            claim = {
                "claim_id": f"CL-{claim_counter:07d}",
                "policy_id": pol["policy_id"],
                "customer_id": pol["customer_id"],
                "product_line": product_line,
                "coverage_amount": coverage_amount,
                "claim_amount": claim_amount,
                "deductible": deductible,
                "net_paid": net_paid,
                "occurrence_date": occurrence_dt.strftime("%Y-%m-%d"),
                "report_date": report_dt.strftime("%Y-%m-%d"),
                "settlement_date": settlement_dt.strftime("%Y-%m-%d"),
                "region": region,
                "state": state,
                "fraud_score": fraud_score,
                "legal_rep_flag": legal_rep_flag
            }
            claims.append(claim)

    print(f"[INFO] Generated {len(claims)} claims (target≈{int(target_total_lambda)})")
    return claims


