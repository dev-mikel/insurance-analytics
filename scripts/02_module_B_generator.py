def generate_retention(
    customers: List[Dict],
    cfg: dict,
    claims_by_customer: Dict[str, int] | None = None
) -> List[Dict]:
    """
    Generate retention / churn info at customer level.
    Expected columns for Module C:
      - customer_id, status, churn_flag, churn_reason,
        churn_date, campaign_executed, campaign_type,
        responded_flag, retained_after_campaign,
        engagement_score, nps_score

    Mejora:
    - churn_prob ajustada por número de siniestros del cliente.
    """
    churn_cfg = cfg["customerModel"]["churn"]
    base_churn_prob = churn_cfg["baseProbability"]
    churn_reasons_dist = churn_cfg["churnReasons"]

    retention_cfg = cfg["retentionModel"]
    campaign_prob = retention_cfg["campaignProbability"]
    campaign_types_dist = retention_cfg["campaignTypes"]
    engagement_dist = retention_cfg["engagementScoreDistribution"]
    nps_dist = retention_cfg["npsDistribution"]

    policy_date_cfg = cfg["policyModel"]["effectiveDateRange"]
    sim_start_year = policy_date_cfg["startYear"]
    sim_end_year = policy_date_cfg["endYear"]

    rows: List[Dict] = []
    claims_by_customer = claims_by_customer or {}

    for cust in customers:
        customer_id = cust["customer_id"]
        num_claims = claims_by_customer.get(customer_id, 0)

        # Ajuste simple: más siniestros => más probabilidad de churn
        claim_factor = 1.0 + 0.3 * min(num_claims, 5)
        churn_prob = clamp(base_churn_prob * claim_factor, 0.0, 0.9)

        churn_flag = random.random() < churn_prob
        if churn_flag:
            status = "Churned"
            churn_reason = random_choice_from_distribution(churn_reasons_dist)
            churn_date_dt = random_date_in_year_range(sim_start_year, sim_end_year)
            churn_date = churn_date_dt.strftime("%Y-%m-%d")
        else:
            status = "Active"
            churn_reason = ""
            churn_date = ""

        # retention campaign?
        campaign_executed = random.random() < campaign_prob
        if campaign_executed:
            campaign_type = random_choice_from_distribution(campaign_types_dist)
            responded_flag = random.random() < 0.5
            # If already churned, campaign may or may not win back
            retained_after_campaign = responded_flag and (not churn_flag)
        else:
            campaign_type = ""
            responded_flag = False
            retained_after_campaign = False

        engagement_score = random.randint(
            engagement_dist["min"], engagement_dist["max"]
        )
        nps_score = random.randint(nps_dist["min"], nps_dist["max"])

        rows.append({
            "customer_id": customer_id,
            "status": status,
            "churn_flag": churn_flag,
            "churn_reason": churn_reason,
            "churn_date": churn_date,
            "campaign_executed": campaign_executed,
            "campaign_type": campaign_type,
            "responded_flag": responded_flag,
            "retained_after_campaign": retained_after_campaign,
            "engagement_score": engagement_score,
            "nps_score": nps_score
        })

    return rows


